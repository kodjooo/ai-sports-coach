"""Разминка и заминка по задействованным группам мышц (без вызова LLM)."""
from __future__ import annotations

# Общая часть — всегда
GENERAL_WARMUP = [
    "Суставная разминка сверху вниз: шея, плечи, локти, кисти, таз, колени, стопы — по 5–8 вращений.",
    "Лёгкое кардио 2–3 минуты: марш на месте, быстрая ходьба или прыжки.",
]
GENERAL_COOLDOWN = [
    "Восстанови дыхание: 5 медленных глубоких вдохов и выдохов.",
]

# Дополнения по ключевым словам в названии группы мышц
WARMUP_BY_GROUP = {
    "грудь": "Махи и вращения руками + 8–10 отжиманий от стены, чтобы разогреть плечи и грудь.",
    "трицепс": "Разгибания рук и вращения в локтях по 10 раз.",
    "ног": "10–15 приседаний без веса в спокойном темпе.",
    "ягод": "Выпады на месте по 8–10 на каждую ногу.",
    "пресс": "Медленные скручивания 10 раз для активации пресса.",
    "кор": "Планка 15–20 секунд как активация кора.",
}
COOLDOWN_BY_GROUP = {
    "грудь": "Растяжка груди в дверном проёме 20–30 секунд.",
    "трицепс": "Растяжка трицепса за головой по 20 секунд на руку.",
    "ног": "Растяжка квадрицепса стоя по 20–30 секунд на ногу.",
    "ягод": "Растяжка ягодиц лёжа (нога на ногу) по 20–30 секунд на сторону.",
    "пресс": "Лёгкий прогиб назад (поза кобры) 20–30 секунд.",
    "кор": "Растяжка спины «кошка-корова», 5–6 плавных циклов.",
}


def _by_group(groups: list[str], mapping: dict[str, str]) -> list[str]:
    """Подбирает подсказки по ключевым словам в названиях групп мышц."""
    found: list[str] = []
    joined = " ".join(g.lower() for g in groups if g)
    for key, text in mapping.items():
        if key in joined and text not in found:
            found.append(text)
    return found


def warmup_text(groups: list[str]) -> str:
    lines = GENERAL_WARMUP + _by_group(groups, WARMUP_BY_GROUP)
    body = "\n".join(f"• {x}" for x in lines)
    return f"🔥 <b>Разминка</b> (3–5 минут, чтобы не травмироваться):\n{body}"


def cooldown_text(groups: list[str]) -> str:
    lines = GENERAL_COOLDOWN + _by_group(groups, COOLDOWN_BY_GROUP)
    body = "\n".join(f"• {x}" for x in lines)
    return f"🧘 <b>Заминка</b> (растяжка задействованных мышц):\n{body}"
